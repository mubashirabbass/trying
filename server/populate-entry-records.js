// Script to populate entry records for existing students
import { db, usersTable, enrollmentsTable, coursesTable, paymentsTable, entryRecordsTable } from "@workspace/db";
import { eq, and, or, desc } from "drizzle-orm";

async function populateEntryRecords() {
  console.log("🚀 Starting to populate entry records for existing students...");
  
  try {
    // Get all active students who have enrollments
    const studentsWithEnrollments = await db
      .select({
        userId: usersTable.id,
        userName: usersTable.name,
        userRegNo: usersTable.regNo,
      })
      .from(usersTable)
      .innerJoin(enrollmentsTable, eq(usersTable.id, enrollmentsTable.userId))
      .where(and(
        eq(usersTable.role, "student"),
        eq(usersTable.isActive, true)
      ))
      .groupBy(usersTable.id, usersTable.name, usersTable.regNo);

    console.log(`📊 Found ${studentsWithEnrollments.length} students with enrollments`);

    if (studentsWithEnrollments.length === 0) {
      console.log("❌ No students with enrollments found");
      return;
    }

    // Process each student
    let createdCount = 0;
    const currentMonth = new Date().toLocaleDateString("en-GB", { 
      month: "long", 
      year: "numeric" 
    });

    for (const student of studentsWithEnrollments) {
      try {
        // Check if entry record already exists for this student
        const existingEntry = await db
          .select({ id: entryRecordsTable.id })
          .from(entryRecordsTable)
          .where(eq(entryRecordsTable.userId, student.userId))
          .limit(1);

        if (existingEntry.length > 0) {
          console.log(`⏭️  Skipping ${student.userName} - entry record already exists`);
          continue;
        }

        // Get student's enrollments with course details
        const enrollments = await db
          .select({
            enrollmentDate: enrollmentsTable.enrolledAt,
            courseTitle: coursesTable.title,
            courseDuration: coursesTable.duration,
          })
          .from(enrollmentsTable)
          .innerJoin(coursesTable, eq(enrollmentsTable.courseId, coursesTable.id))
          .where(eq(enrollmentsTable.userId, student.userId))
          .orderBy(desc(enrollmentsTable.enrolledAt));

        if (enrollments.length === 0) continue;

        // Get student's payments
        const payments = await db
          .select({
            amount: paymentsTable.amount,
            createdAt: paymentsTable.createdAt,
          })
          .from(paymentsTable)
          .where(and(
            eq(paymentsTable.userId, student.userId),
            or(
              eq(paymentsTable.status, "approved"),
              eq(paymentsTable.status, "verified"),
              eq(paymentsTable.status, "completed")
            )
          ))
          .orderBy(paymentsTable.createdAt);

        // Calculate totals
        const totalAmount = payments.reduce((sum, p) => sum + p.amount, 0);
        const firstEnrollment = enrollments[0];
        const receiptNumbers = payments.length > 0 
          ? payments.map((_, index) => `RCP-${String(index + 1).padStart(3, '0')}`).join(", ")
          : "PENDING";

        // Create entry record
        const [entryRecord] = await db
          .insert(entryRecordsTable)
          .values({
            studentName: student.userName,
            histNo: student.userRegNo || "",
            course: enrollments.map(e => e.courseTitle).join(", "),
            admissionDate: firstEnrollment.enrollmentDate?.toLocaleDateString("en-GB") || "",
            duration: enrollments.map(e => e.courseDuration).join(", "),
            receiptNo: receiptNumbers,
            amount: totalAmount || 0,
            month: currentMonth,
            isAutoGenerated: true,
            userId: student.userId,
          })
          .returning();

        console.log(`✅ Created entry record for: ${student.userName} (Amount: Rs.${totalAmount})`);
        createdCount++;

      } catch (studentError) {
        console.error(`❌ Error processing ${student.userName}:`, studentError.message);
      }
    }

    console.log(`🎉 Successfully created ${createdCount} entry records!`);
    
  } catch (error) {
    console.error("❌ Error populating entry records:", error);
  }
}

// Run the script
populateEntryRecords().then(() => {
  console.log("✅ Entry records population completed");
  process.exit(0);
}).catch((error) => {
  console.error("❌ Script failed:", error);
  process.exit(1);
});